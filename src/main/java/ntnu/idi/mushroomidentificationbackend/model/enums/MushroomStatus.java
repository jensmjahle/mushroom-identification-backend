package ntnu.idi.mushroomidentificationbackend.model.enums;

/**
 * Enum representing the status of a mushroom.
 * This is used to indicate whether a mushroom is
 * psilocybin, non-psilocybin, toxic, unknown,
 * unidentifiable, has bad pictures, or has not been processed.
 */
public enum MushroomStatus {
  PSILOCYBIN,
  NON_PSILOCYBIN,
  TOXIC,
  UNKNOWN,
  UNIDENTIFIABLE,
  BAD_PICTURES,
  NOT_PROCESSED
}
